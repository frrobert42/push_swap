#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	char		*temp;
	size_t		i;

	i = 0;
	temp = b;
	while (i < n)
	{
		temp[i] = (char)c;
		i++;
	}
	return (b);
}
